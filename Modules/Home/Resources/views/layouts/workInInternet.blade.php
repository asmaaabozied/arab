                                                        من الطلبيات في وقت واحد، غير أنه تلك المواقع تعطي خصومات إذا قمت بتقييم القطع الخاصة بها وما إلى ذلك.</p>
                                                    <p>وقد تتسائلون كيف يمكن إذا أن يكون البائع براء تلك القطع الأساسية بدون رأس مال، حينها سأخبركم أن سياسات البيع قد تغيرت كثيرا فأصبح البائع لا يشتري القطعة إلا بعد أن يعطيه المشتري نصف ثمنها كي يضمن
                                                        حقه وحق المشتري.</p>
