                                                        أي دولة تقوم تلك المواقع بالشحن إليها مجانيا، ويشحن تلك الملابس علي بيته، ومن ثم ينتظر أن ينزل هذا الشخص لمصر وهذا سيجنبه صرف الكثير من الأموال.</p>
                                                    <p>ولكن سيتبادر إلي ذهنكم الآن كيف يمكن أن يكون هناك هامش ربح بعد كل تلك المصاريف؟</p>
